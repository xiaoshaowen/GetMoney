


CREATE TABLE `wz_global_config` (
	`id` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '设置项主键',
	`m` varchar(30) NULL COMMENT '模块名',
	`modelid` int UNSIGNED  NULL COMMENT '模块名',
	`pid` INT UNSIGNED NULL COMMENT '父级主键',
	`name` varchar(10)  NULL COMMENT '名称',
	`remark` varchar(255) NULL  COMMENT '描述',
	`sort` int UNSIGNED  NULL COMMENT '排序id',
	primary key (id)
)
COLLATE='gbk_chinese_ci'
ENGINE=MyISAM;

